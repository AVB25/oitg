r""":class:`otig.circuits.clifford.GateGroup` implementation for the qutrit Clifford
group as implemented by global operations on a pair of qubits.
"""

from .clifford import CliffordImpl, GateGroup, to_canonical_matrix_key
from .gate import Gate, GateGenerator, GateSequence
from .to_matrix import gate_sequence_matrix
import numpy as np
from typing import Dict, List

to_qutrit = np.array([
    [1, 0, 0, 0],
    [0, 1 / np.sqrt(2), 1 / np.sqrt(2), 0],
    [0, 0, 0, 1],
])


class SymQutritGateGroup(GateGroup):
    """Gate group in the symmetric (qutrit) subspace, where identity is only up to the
    action on the additional antisymmetric degree of freedom.
    """
    def __init__(self, gate_sequences: List[GateSequence], matrices: List[np.ndarray],
                 inverse_idxs: Dict[bytearray, int]):
        super().__init__(num_qubits=2,
                         gate_sequences=gate_sequences,
                         matrices=matrices,
                         inverse_idxs=inverse_idxs)

    def find_inverse_idx(self, matrix: np.ndarray) -> int:
        u_qutrit = to_qutrit @ matrix @ to_qutrit.T
        return super().find_inverse_idx(u_qutrit)

    def pauli_idxs(self) -> List[int]:
        # The elements of the generalized Pauli (Heisenberg-Weyl) group are those which
        # are a cyclic shift (indexed by i) with the product of all phases being unity
        # (the choices being indexed by j).
        return [i + j for i in range(2) for j in [0, 15, 21]]


def make_sym_qutrit_clifford_group(implementation: CliffordImpl) -> GateGroup:
    """Construct a :class:`GateGroup` instance which enumerates all elements of the
    qutrit Clifford group, backed by a particular implementation in terms of global
    two-qubit operations.
    """

    gate_sequences = []
    matrices = []
    inverse_idxs = {}
    for idx in range(216):
        gates = list(implementation(idx))
        gate_sequences.append(gates)
        u_gate = gate_sequence_matrix(gates, 2)
        u_qutrit = to_qutrit @ u_gate @ to_qutrit.T
        matrices.append(u_gate)
        inverse_idxs[to_canonical_matrix_key(np.linalg.inv(u_qutrit))] = idx

    return SymQutritGateGroup(gate_sequences, matrices, inverse_idxs)


def _get_impl(data: list, entangling_gate: Gate, idx: int) -> GateGenerator:
    """Return gate sequence for the Clifford group element with the given index using
    the specified single-qubit angle data tables and entangling gate.

    The decompositions aren't feasible to determine in real time (at least not without a
    whole lot more math than went into DPN's exhaustive search script for coming up with
    these), so we just store the necessary single-qubit rotation angles.

    More precisely, the data table stores, for each Clifford element, a sequence of
    blocks of arbitrary single-qubit rotations, which are join()ed by the specified
    entangling gate. The single-qubit rotations are specified in terms of Euler angles,
    which have a straightforward implementation in terms of the fixed X/Z rotation
    sequence. This always uses two physical Ï€ / 2 pulses, which might not be quite
    optimal, but should be close enough given that the entangling gates will
    realistically have a much larger error.
    """
    first = True
    for run_phases in data[idx][::-1]:
        if first:
            first = False
        else:
            yield entangling_gate
        for qubit in (0, 1):
            yield Gate("rz", (run_phases[2], ), (qubit, ))
            yield Gate("rx", (np.pi / 2, ), (qubit, ))
            yield Gate("rz", (run_phases[0], ), (qubit, ))
            yield Gate("rx", (-np.pi / 2, ), (qubit, ))
            yield Gate("rz", (run_phases[1], ), (qubit, ))


